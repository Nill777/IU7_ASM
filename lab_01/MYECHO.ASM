.MODEL TINY               ; модель памяти для COM-файла
.CODE
ORG 100h                  ; организация кода для COM-файла

start:
    mov dx, 80h           ; адрес начала аргументов командной строки PSP
    mov cl, [byte ptr ds:80h] ; первый байт, указывающий на длину аргумента
    mov ch, 0             ; CH:CL теперь содержит длину строки

    inc dx                ; пропустить первый байт с длиной строки

print_args:                ; метка цикла для печати аргументов
    mov al, [dx]          ; получить текущий символ аргумента
    inc dx                ; переход к следующему символу

    cmp al, ' '           ; проверить, является ли символ пробелом
    je  print_args        ; если да, пропустить его

    cmp al, 0             ; проверить, является ли это концом строки
    je  done              ; если да, то закончить

    mov ah, 0Eh           ; функция teletype BIOS для печати символа на экран
    int 10h               ; вызвать прерывание для вывода символа

    loop print_args       ; повторять пока CL не равно нулю

done:
    mov ax, 4C00h         ; завершить программу с кодом возврата 0
    int 21h               ; вызов DOS для завершения программы

END start
